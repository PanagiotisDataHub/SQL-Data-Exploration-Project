/*
Question: What skills are required for the top-paying Data Analyst jobs?
- Analyze the top 10 highest-paying Data Analyst roles identified in Query 1.
- Identify the specific skills associated with these top-paying positions by joining job postings with the skills table.
- Why? This helps job seekers understand which skills are most valuable for securing high-salary Data Analyst roles, 
    allowing them to strategically develop the right expertise to maximize their earning potential.
*/

WITH top_paying_jobs AS (
    SELECT
        job_id,
        job_title,
        salary_year_avg,
        name AS company_name
    FROM 
        job_postings_fact
    LEFT JOIN company_dim ON job_postings_fact.company_id = company_dim.company_id
    WHERE
        job_title_short = 'Data Analyst' 
        AND job_location = 'Anywhere' 
        AND salary_year_avg IS NOT NULL
    ORDER BY
        salary_year_avg DESC
    LIMIT 10
)

SELECT 
    top_paying_jobs.*,
    skills
FROM 
    top_paying_jobs
INNER JOIN skills_job_dim ON top_paying_jobs.job_id = skills_job_dim.job_id
INNER JOIN skills_dim ON skills_job_dim.skill_id = skills_dim.skill_id
ORDER BY
    salary_year_avg DESC;

